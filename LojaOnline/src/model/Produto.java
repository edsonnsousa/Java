package model;

public class Produto {
    private int codigo;
    private double valor;
    private String categoria;
    private int qtdd;

    public String getCategoria() {
        return categoria;
    }

    public int getQtdd() {
        return qtdd;
    }

    public int getCodigo() {
        return codigo;
    }

    public double getValor() {
        return valor;
    }

}
